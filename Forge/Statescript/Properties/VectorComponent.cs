namespace Gamesmiths.Forge.Statescript.Properties;

/// <summary>
/// Represents the individual components of a vector, such as X, Y, Z, and W. It is used to specify which component of a
/// vector is being accessed or modified in various operations within the Statescript system.
/// </summary>
public enum VectorComponent
{
	/// <summary>
	/// The X component of the vector.
	/// </summary>
	X = 0,

	/// <summary>
	/// The Y component of the vector.
	/// </summary>
	Y = 1,

	/// <summary>
	/// The Z component of the vector.
	/// </summary>
	Z = 2,

	/// <summary>
	/// The W component of the vector.
	/// </summary>
	W = 3,
}
